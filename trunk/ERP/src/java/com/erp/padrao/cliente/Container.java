     public static void setContainer(UsuarioVO usuarioVO) {
        container.put("usuario", usuarioVO);

        if (usuarioVO.getColaborador().getPessoa().getListaEmpresa().size() == 1) {
            container.put("empresa", usuarioVO.getColaborador().getPessoa().getListaEmpresa().get(0));
        } else if (usuarioVO.getColaborador().getPessoa().getListaEmpresa().size() > 1) {
            EmpresaVO empresas[] = new EmpresaVO[usuarioVO.getColaborador().getPessoa().getListaEmpresa().size()];
            usuarioVO.getColaborador().getPessoa().getListaEmpresa().toArray(empresas);
            EmpresaVO empresa = null;
            while (empresa == null) {
                empresa = (EmpresaVO) JOptionPane.showInputDialog(null,
                        "Selecione uma empresa:",
                        "Solicitação do Sistema",
                        JOptionPane.QUESTION_MESSAGE,
                        null,
                        empresas,
                        empresas[0]);
            }
            container.put("empresa", empresa);
        }
    }
    
    
    
