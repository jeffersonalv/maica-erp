@Entity
@Table(name = "arquivo")
public class ArquivoVO extends ValueObjectImpl implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "ID")
    private Integer id;
    @Lob
    @Column(name = "CAMINHO")
    private String caminho;
    @Column(name = "DATA_ENVIO")
    @Temporal(TemporalType.DATE)
    private Date dataEnvio;
    @Column(name = "HORA_ENVIO")
    private String horaEnvio;
    @JoinColumn(name = "ID_CHAMADO", referencedColumnName = "ID")
    @ManyToOne(optional = false)
    private ChamadoVO chamado;
    @Transient
    private byte[] arquivo;
    @Transient
    private String extensao;

    public ArquivoVO() {
    }

    public ArquivoVO(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getCaminho() {
        return caminho;
    }

    public void setCaminho(String caminho) {
        this.caminho = caminho;
    }

    public Date getDataEnvio() {
        return dataEnvio;
    }

    public void setDataEnvio(Date dataEnvio) {
        this.dataEnvio = dataEnvio;
    }

    public String getHoraEnvio() {
        return horaEnvio;
    }

    public void setHoraEnvio(String horaEnvio) {
        this.horaEnvio = horaEnvio;
    }

    @Override
    public String toString() {
        return "com.t2tierp.suporte.java.ArquivoVO[id=" + id + "]";
    }

    /**
     * @return the chamado
     */
    public ChamadoVO getChamado() {
        return chamado;
    }

    /**
     * @param chamado the chamado to set
     */
    public void setChamado(ChamadoVO chamado) {
        this.chamado = chamado;
    }

    public byte[] getArquivo() {
        return arquivo;
    }

    public void setArquivo(byte[] arquivo) {
        this.arquivo = arquivo;
    }

    public String getExtensao() {
        return extensao;
    }

    public void setExtensao(String extensao) {
        this.extensao = extensao;
    }

}
